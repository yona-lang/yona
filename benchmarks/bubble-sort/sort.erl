min(X, Y) when X < Y -> X;
min(X, Y) -> Y.

max(X, Y) when X < Y -> Y;
max(X, Y) -> X.

bubble_up([]) -> [];
bubble_up([X]) -> [X];
bubble_up([X, Y | XS]) ->
  Smaller = min(X, Y),
  Larger = max(X, Y),

  [Smaller | bubble_up([Larger | XS])].

bubble_sort(L) -> lists:foldl(fun(X, Acc) -> bubble_up(Acc) end, L, lists:seq(1, length(L))).

